package project_name;

public class class1 {
    public static void main(String[] args) {
        System.out.println("hello-world"+"3");
        System.out.println(3+3);
        // 큰따옴표는 문장, 문자는 작은따옴표
        // 특수문자를 넣고 싶으면 앞에 \ 붙이기
        System.out.println("\"hello\"");
        // \n을 사용하면 엔터를 칠 수 있습니다. \t :탭
        System.out.println("hello\nworld");
    }
}
